import React, { Component } from 'react';


//// IMPORT COMPONENTS, STYLES ////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////

import './NewForm.css';





//// COMPONENT CLASS /////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////

class NewForm extends Component {

//// CONSTRUCTOR //////////////////////////////////////////////////////////
  constructor() {
    super()
    this.state = {
      newUrl: ''
    }
  }



//// ONCHANGE//////////////////////////////////////////////////////////////

  handleChange = (e) => {
    this.setState({
      newUrl: e.target.value
    })
    console.log(this.state.newUrl)
  }

//// SUBMIT //////////////////////////////////////////////////////////////

  handleSubmit = (e) => {
    e.preventDefault()
    this.props.addNew(this.state.newUrl)
  }


//// RENDER //////////////////////////////////////////////////////////////
  render() {

//// RETURN //////////////////////////////////////////////////////////////
    return (
      <div className="NewFormDiv">

        <form onSubmit={this.handleSubmit.bind(this)}>

          <label>Author:</label>
          <input type="text" value="Spen" readOnly/>

          <label>Photo:</label>
          <input type="text" placeholder="enter image url" onChange={(e) => this.handleChange(e)} />

          <input type="submit" value="Submit" />

        </form>

      </div>
    );
  }
}



//// EXPORT COMPONENT ////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////

export default NewForm;





